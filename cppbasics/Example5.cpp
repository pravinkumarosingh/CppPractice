#include<iostream>
using namespace std;

int main(){
    int a=13, b=5;

    cout << "Addition is " << a+b << "\n";
    cout << "Subtraction is " << a-b << "\n";
    cout << "Multiplication is " << a*b << "\n";
    cout << "Quotient is " << (float) a/b << "\n";
    cout << "Remainder is " << a%b << "\n";
    return 0;
}